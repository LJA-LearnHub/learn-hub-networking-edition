/** lessonId -> chapter objects (for supplements on curriculum lessons) */
export function buildLessonToChaptersMap() {
  const map = new Map();
  for (const ch of HTN_CHAPTERS) {
    for (const id of ch.alsoIn || []) {
      if (!map.has(id)) map.set(id, []);
      map.get(id).push(ch);
    }
  }
  return map;
}
