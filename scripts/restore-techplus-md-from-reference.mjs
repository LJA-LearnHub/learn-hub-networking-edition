/**
 * Merges **missing** Tech+ study-guide HTML (tech-sg-*) from the All_Learn-hub **monolith**
 * into the local chapter chunk files. Does **not** copy learn-hub-courses.js — lesson titles and
 * IDs must come from `npm run build:techplus` so sidebar labels match embedded segment banners.
 *
 * Run: node scripts/restore-techplus-md-from-reference.mjs
 */
import fs from "fs";
import path from "path";
import vm from "vm";
import { fileURLToPath } from "url";
import {
  readMergedChapterMaps,
  writeChapterJsFiles,
  writeTechplusMdLoader,
} from "./techplus-chapter-io.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const learnHubRoot = path.resolve(__dirname, "..");

const REF_ROOT = "g:/All_Learn-hub/Learn-Hub";
const REF_COURSES = path.join(REF_ROOT, "learn-hub-courses.js");
const REF_MONO = path.join(REF_ROOT, "learn-hub-techplus-md.js");

const banner =
  "/* Auto-generated by scripts/build-techplus-from-markdown.mjs — edit chapters/*.md then re-run */\n" +
  "/* Patched: restore-techplus-md-from-reference.mjs merged missing tech-sg segments from All_Learn-hub monolith */\n";

function loadCourses(p) {
  const s = fs.readFileSync(p, "utf8");
  const ctx = { window: {} };
  vm.runInNewContext(s.replace(/^[^=]+=/, "window.LEARN_HUB_COURSES ="), ctx);
  return ctx.window.LEARN_HUB_COURSES;
}

function techSgIdsFromCourses(courses) {
  const tech = courses.find((c) => c.id === "tech");
  if (!tech || !tech.lessons) return [];
  return tech.lessons.map((L) => L.id).filter((id) => /^tech-sg-\d{2}-\d{2}$/.test(id));
}

function loadReferenceMonolith() {
  const s = fs.readFileSync(REF_MONO, "utf8");
  if (/loadLearnHubTechplusChapter\s*\(/.test(s) && /__LH_TECHPLUS_CH_HASH/.test(s)) {
    throw new Error(
      "Reference learn-hub-techplus-md.js is a loader, not a monolith — use All_Learn-hub/Learn-Hub copy with a full object."
    );
  }
  const ctx = { window: {} };
  vm.runInNewContext(s.replace(/^[^=]+=/, "window.LEARN_HUB_TECHPLUS_MD ="), ctx);
  return ctx.window.LEARN_HUB_TECHPLUS_MD || {};
}

function main() {
  if (!fs.existsSync(REF_COURSES) || !fs.existsSync(REF_MONO)) {
    console.error("Missing reference files under", REF_ROOT);
    process.exit(1);
  }

  const destCourses = path.join(learnHubRoot, "assets", "learn-hub-courses.js");
  if (!fs.existsSync(destCourses)) {
    console.error("Missing", destCourses, "— run npm run build:techplus first.");
    process.exit(1);
  }

  const courses = loadCourses(destCourses);
  const want = new Set(techSgIdsFromCourses(courses));
  console.log("Tech+ study-guide lesson IDs expected:", want.size);

  const merged = readMergedChapterMaps(learnHubRoot);
  const ref = loadReferenceMonolith();

  let added = 0;
  const stillMissing = [];
  for (const id of want) {
    if (!ref[id]) {
      stillMissing.push(id);
      continue;
    }
    if (!merged[id]) {
      merged[id] = ref[id];
      added++;
    }
  }

  if (stillMissing.length) {
    console.error("Reference monolith missing HTML for:", stillMissing.join(", "));
    process.exit(1);
  }

  const chHashes = writeChapterJsFiles(learnHubRoot, merged, banner);
  writeTechplusMdLoader(learnHubRoot, chHashes, banner);

  console.log("Wrote 12 chapter chunk files + learn-hub-techplus-md.js loader.");
  console.log("Inserted missing tech-sg segments (were absent from chunks):", added);
}

main();
